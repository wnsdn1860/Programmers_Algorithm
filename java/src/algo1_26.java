/*
 * 문제 설명 ( 두 정수 사이의 합 )
두 정수 a, b가 주어졌을 때 a와 b 사이에 속한 모든 정수의 합을 리턴하는 함수, solution을 완성하세요.
예를 들어 a = 3, b = 5인 경우, 3 + 4 + 5 = 12이므로 12를 리턴합니다.

제한 조건
a와 b가 같은 경우는 둘 중 아무 수나 리턴하세요.
a와 b는 -10,000,000 이상 10,000,000 이하인 정수입니다.
a와 b의 대소관계는 정해져있지 않습니다.
 * */
public class algo1_26 {
	public long solution(int a, int b) {
        long result = 0;
        long num1 = (long) a;
        long num2 = (long) b;
        if(num1 == num2) return num1; // 똑같으면 둘중 아무거나 리턴
        if(num1 > num2) {
            result = ((num1 * (num1+1)) / 2) -  ((num2 * (num2-1)) / 2);
        }
        else {
            result = ((num2 * (num2+1)) / 2) -  ((num1 * (num1-1)) / 2);
        }
        return result;
    }
	public static void main(String[] args) { // 테스트케이스 확인용 main 코드 
		algo1_26 algo = new algo1_26();
		long result = algo.solution(3, 5);
		if(result == 12) System.out.println("맞았습니다.");
	}

}
